    tabula_recta = creare_tabula_recta()
    text = text.upper()
    text_criptat = ""
    i = 1

    for char in text:
        if char in alfabet:
            index_alfabet = alfabet.index(char)
            coloana = index_alfabet
            linie = i % 26
            litera_criptata = tabula_recta[linie][coloana]
            text_criptat += litera_criptata
