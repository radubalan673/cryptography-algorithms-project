def creare_tabula_recta():
    alfabet = "ABCDEFGHIJKLMNOPQRSTUVWXYZ"
    tabel = []

    for decalare in range(26):
        linie = []  # Creăm fiecare linie

        for i in range(26):
            index = (i + decalare) % 26  # Calculăm poziția pentru fiecare literă
            linie.append(alfabet[index])  # Adăugăm litera în linie

        tabel.append(linie)  # Adăugăm linia în tabel

    return tabel

def afisare_tabula_recta(tabel):
    for linie in tabel:
        print(" ".join(linie))

# Creăm tabula recta și o afișăm
tabula = creare_tabula_recta()
