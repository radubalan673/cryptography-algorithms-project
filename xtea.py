      sum = (sum + delta) & 0xFFFFFFFF
      v0 = (v0 + (((v1 << 4 ^ v1 >> 5) + v1) ^ (sum + key[sum & 3]))) & 0xFFFFFFFF
      v1 = (v1 + (((v0 << 4 ^ v0 >> 5) + v0) ^ (sum + key[(sum >> 11) & 3]))) & 0xFFFFFFFF

