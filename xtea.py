def imparte_in_blocuri_de_8_bytes(mesaj):
    lista_blocuri = []
    mesaj_bytes = mesaj.encode('utf-8')

    i = 0
    rest = len(mesaj_bytes) % 8
    while i + 8 < len(mesaj_bytes):
      bloc = b''
      for index in range(i,i+8):
        bloc += bytes([mesaj_bytes[index]])
      lista_blocuri.append(bloc)
      i = i + 8

    if rest != 0:
      bloc = b''
      for index in range(i , i + rest):
        bloc += bytes([mesaj_bytes[index]])

      nr_spatii_necesar = 8 - len(bloc)
      for spatiu in range(0, nr_spatii_necesar):
        bloc += b' '

      lista_blocuri.append(bloc)
    return lista_blocuri



def generate_key():
    random_key = os.urandom(16)
    key = []

    index = 0
    while index < 16:
      subcheie = b''
      for byte in range(index,index + 4):
        subcheie += bytes([random_key[byte]])
      subkey = int.from_bytes(subcheie, 'big')
      key.append(subkey)
      index = index + 4

    return key

